# ─────────────────────────── replay (dry-run + apply) ───────────────────────────


def test_replay_dry_run_does_not_mutate(redis_with_entries):
    args = type("A", (), {"msg_id": "1000-1", "apply": False, "keep": False})()
    rc, out = _run(lambda r: dlq_replay.cmd_replay(r, args), redis_with_entries)
    assert rc == 0
    assert "DRY RUN" in out and "WOULD XADD" in out
    # nothing changed
    assert len(redis_with_entries.streams[dlq_replay.DLQ_STREAM]) == 2
    assert "events:aim.source.created" not in redis_with_entries.streams


def test_replay_apply_mutates_and_purges(redis_with_entries):
    args = type("A", (), {"msg_id": "1000-1", "apply": True, "keep": False})()
    rc, out = _run(lambda r: dlq_replay.cmd_replay(r, args), redis_with_entries)
    assert rc == 0
    assert "DID XADD" in out
    # source stream got the event back
    src = redis_with_entries.streams["events:aim.source.created"]
    assert len(src) == 1
    assert src[0][1]["event_id"] == "evt-abc"
    # DLQ entry purged
    assert all(mid != "1000-1" for mid, _ in redis_with_entries.streams[dlq_replay.DLQ_STREAM])


def test_replay_apply_keep_does_not_purge(redis_with_entries):
    args = type("A", (), {"msg_id": "2000-1", "apply": True, "keep": True})()
    rc, _ = _run(lambda r: dlq_replay.cmd_replay(r, args), redis_with_entries)
    assert rc == 0
    assert any(mid == "2000-1" for mid, _ in redis_with_entries.streams[dlq_replay.DLQ_STREAM])
    assert "generation:jobs" in redis_with_entries.streams


