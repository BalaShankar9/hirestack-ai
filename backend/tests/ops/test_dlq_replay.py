# ─────────────────────────── fake redis ───────────────────────────


class _FakeRedis:
    """Tiny synchronous Redis-stream stub.

    Implements just enough of XRANGE / XADD / XDEL for the script.
    Stream entries are stored as ``(msg_id, fields_dict)`` lists keyed
    by stream name. Auto-incrementing msg_ids of the form ``<ms>-N`` are
    generated by ``xadd`` unless ``msg_id`` is provided via ``_seed``.
    """

    def __init__(self) -> None:
        self.streams: dict[str, list[tuple[str, dict[str, str]]]] = {}
        self._seq = 0

    def ping(self) -> bool:
        return True

    def _next_id(self) -> str:
        self._seq += 1
        return f"{int(time.time() * 1000)}-{self._seq}"

    def xadd(self, stream: str, fields: dict[str, str]) -> str:
        mid = self._next_id()
        self.streams.setdefault(stream, []).append((mid, dict(fields)))
        return mid

    def _seed(self, stream: str, mid: str, fields: dict[str, str]) -> None:
        self.streams.setdefault(stream, []).append((mid, dict(fields)))

    def xrange(self, stream, *, min="-", max="+", count=None):
        entries = list(self.streams.get(stream, []))
        if min != "-":
            entries = [e for e in entries if e[0] >= min]
        if max != "+":
            entries = [e for e in entries if e[0] <= max]
        if count is not None:
            entries = entries[:count]
        return entries

    def xdel(self, stream: str, mid: str) -> int:
        before = len(self.streams.get(stream, []))
        self.streams[stream] = [(m, f) for m, f in self.streams.get(stream, []) if m != mid]
        return before - len(self.streams.get(stream, []))


