@pytest.fixture
def redis_with_entries():
    r = _FakeRedis()
    # Generic-consumer entry (has 'event' field).
    r._seed(
        dlq_replay.DLQ_STREAM,
        "1000-1",
        {
            "consumer": "outbox-relay",
            "source_stream": "events:aim.source.created",
            "source_msg_id": "999-1",
            "reason": "handler raised: NoCreditsError",
            "event": json.dumps(
                {
                    "event_id": "evt-abc",
                    "event_type": "aim.source.created",
                    "event_version": 1,
                    "payload": {"source_id": "src-xyz"},
                }
            ),
        },
    )
    # Queue entry (no 'event' field — has job_id/user_id).
    r._seed(
        dlq_replay.DLQ_STREAM,
        "2000-1",
        {
            "consumer": "hirestack-workers",
            "source_stream": "generation:jobs",
            "source_msg_id": "1500-1",
            "job_id": "job-42",
            "user_id": "user-7",
            "reason": "max_deliveries_exceeded (6>5)",
        },
    )
    return r


def _run(args_func, redis):
    buf = io.StringIO()
    with redirect_stdout(buf):
        rc = int(args_func(redis))
    return rc, buf.getvalue()


# ─────────────────────────── parse helpers ───────────────────────────


def test_parse_since_dash_means_all():
    assert dlq_replay._parse_since("-") == "-"
    assert dlq_replay._parse_since(None) == "-"


def test_parse_since_durations():
    out = dlq_replay._parse_since("15m")
    ms = int(out.split("-")[0])
    assert ms <= int(time.time() * 1000) - 14 * 60 * 1000


def test_parse_since_bare_epoch():
    assert dlq_replay._parse_since("1700000000000") == "1700000000000-0"


def test_parse_since_bad_input(capsys):
    with pytest.raises(SystemExit) as exc:
        dlq_replay._parse_since("garbage")
    assert exc.value.code == 3


def test_matches_filters():
    f = {"consumer": "x", "reason": "Boom because of NoCredits"}
    assert dlq_replay._matches_filters(f, consumer="x", reason_substr="nocredits")
    assert not dlq_replay._matches_filters(f, consumer="y", reason_substr=None)
    assert not dlq_replay._matches_filters(f, consumer=None, reason_substr="missing")


